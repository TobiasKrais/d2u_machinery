    private function getReplacementValue(string $key, int $clang_id): string
    {
        $lang_replacement = rex_config::get('d2u_machinery', 'lang_replacement_'. $clang_id, '');

        if ('chinese' === $lang_replacement && isset($this->replacements_chinese[$key])) {
            return $this->replacements_chinese[$key];
        }
        if ('dutch' === $lang_replacement && isset($this->replacements_dutch[$key])) {
            return $this->replacements_dutch[$key];
        }
        if ('french' === $lang_replacement && isset($this->replacements_french[$key])) {
            return $this->replacements_french[$key];
        }
        if ('german' === $lang_replacement && isset($this->replacements_german[$key])) {
            return $this->replacements_german[$key];
        }
        if ('portuguese' === $lang_replacement && isset($this->replacements_portuguese[$key])) {
            return $this->replacements_portuguese[$key];
        }
        if ('russian' === $lang_replacement && isset($this->replacements_russian[$key])) {
            return $this->replacements_russian[$key];
        }
        if ('spanish' === $lang_replacement && isset($this->replacements_spanish[$key])) {
            return $this->replacements_spanish[$key];
        }

        return $this->replacements_english[$key];
    }

    /**
     * @param array<int,string> $keys
     */
    /**
     * Factory method.
    * @return LangHelper Object
     */
    public static function factory()
    {
        return new self();
    }

    /**
     * Installs the replacement table for this addon.
     */
    public function install(): void
    {
        $overwrite = 'true' === rex_config::get('d2u_machinery', 'lang_wildcard_overwrite', false);
        foreach ($this->replacements_english as $key => $value) {
            unset($value);
            foreach (rex_clang::getAllIds() as $clang_id) {
                parent::saveValue($key, $this->getReplacementValue($key, $clang_id), $clang_id, $overwrite);
            }
        }
    }

    public function uninstall($clang_id = 0): void
    {
        parent::uninstall($clang_id);
    }
